    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       //Update Button
        try {
            if (jTable1.getSelectedRow() < 0) {
                JOptionPane.showMessageDialog(this, "Please select the row you want to update");
            } else {
                DefaultTableModel mytable = (DefaultTableModel) jTable1.getModel();
                String name = jTextField1.getText();
                String gender = null;
                if (jRadioButton1.isSelected()){gender = "Male";}
                if (jRadioButton2.isSelected()){gender = "Female";}
                int contact = Integer.parseInt(jTextField3.getText());
                String address = jTextField4.getText();
                int row = jTable1.getSelectedRow();
                System.out.println(row);
                String query = "Update `information` Set `Name` = ?, `Gender` = ?, `Contact No.` = ?, `Address` = ? WHERE ID = ?;";
                ps = con.prepareStatement(query);
                ps.setString(1, name);
                ps.setString(2, gender);
                ps.setInt(3, contact);
                ps.setString(4, address);
                ps.setInt(5, (row + 1));
                ps.executeUpdate();
                mytable.setValueAt(name, row, 0);
                mytable.setValueAt(gender, row, 1);
                mytable.setValueAt(contact, row, 2);
                mytable.setValueAt(address, row, 3);
            }
        } catch (SQLException | NumberFormatException ex) {
            System.out.println("Error" + ex);
            JOptionPane.showMessageDialog(this, "Please input a number in Contact No.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

