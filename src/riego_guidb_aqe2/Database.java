    public void getData() {
        try {
            String query = "SELECT * FROM `information`;";
            DefaultTableModel mytable = (DefaultTableModel) jTable1.getModel();
            se = con.prepareStatement(query);
            rs = se.executeQuery(query);
            while (rs.next()) {
                String name = rs.getString(1);
                String gender = rs.getString(2);
                int contact = rs.getInt(3);
                String Address = rs.getString(4);
                int Pid = rs.getInt(5);
                System.out.println("Name: " + name + "\t"
                        + "Gender: " + gender + "\t"
                        + "Contact No.: " + contact + "\t"
                        + "Address: " + Address + "\t"
                        + "ID: " + Pid + "\t");
                mytable.addRow(new Object[]{
                    name,
                    gender,
                    contact,
                    Address,
                    Pid});
            }
        } catch (SQLException ex) {
            System.out.println("Error" + ex);
        }
    }
    
    
    
